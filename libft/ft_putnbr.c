#include "libft.h"
#include <unistd.h>

void	ft_putnbr(int n)
{
	if (n <= -2147483648)
	{
		write(1, "-2147483648", 12);
		return ;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + 48);
}
