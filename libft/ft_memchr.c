#include "libft.h"
#include <string.h>
#include <unistd.h>
#include <stdio.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)c)
		{
			return ((void*)(s + i));
		}
		i++;
	}
	return (NULL);
}
