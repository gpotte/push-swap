#include "libft.h"

char	*ft_toupper(char *line)
{
	int i;

	i = 0;
	while (line[i])
	{
		if (line[i] > 96 && line[i] < 123)
			line[i] = line[i] - 32;
		i++;
	}
	return (line);
}
